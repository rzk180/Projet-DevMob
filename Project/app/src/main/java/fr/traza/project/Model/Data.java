package fr.traza.project.Model;

public class Data {
    String title;
    String place;
    String salary;
    String description;
    String skills;

    String id;
    String date;

    public Data(){

    }

    public Data(String title, String place, String salary, String description, String skills, String id, String date) {
        this.title = title;
        this.place = place;
        this.salary = salary;
        this.description = description;
        this.skills = skills;
        this.id = id;
        this.date = date;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getPlace() {
        return place;
    }

    public void setPlace(String place) {
        this.place = place;
    }

    public String getSalary() {
        return salary;
    }

    public void setSalary(String salary) {
        this.salary = salary;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getSkills() {
        return skills;
    }

    public void setSkills(String skills) {
        this.skills = skills;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
